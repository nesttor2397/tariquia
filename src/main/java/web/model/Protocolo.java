package web.model;

public class Protocolo {
	private int codprot;
	private String nombre;
	private boolean estado;

	public int getCodprot() {
		return codprot;
	}

	public void setCodprot(int codprot) {
		this.codprot = codprot;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public boolean isEstado() {
		return estado;
	}

	public void setEstado(boolean estado) {
		this.estado = estado;
	}

}
